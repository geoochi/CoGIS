//显示图层菜单响应函数
void CMainFrame::OnTcboard()
{
	// TODO: 在此添加命令处理程序代码
	if (IsShowTCPanel)
	{
		m_wndDBView.ShowPane(FALSE,FALSE,TRUE);
		IsShowTCPanel=false;
	}
	else
	{
		m_wndDBView.ShowPane(TRUE,FALSE,TRUE);
		IsShowTCPanel=true;
	}

}

//显示图层菜单响应勾选函数
void CMainFrame::OnUpdateTcboard(CCmdUI *pCmdUI)
{
	// TODO: 在此添加命令更新用户界面处理程序代码
	if (IsShowTCPanel)
	{
		pCmdUI->SetCheck(1);
	}
	else
		pCmdUI->SetCheck(0);
}
